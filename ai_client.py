# DeepSeek API (OpenAI-compatible)
API_URL = "https://api.deepseek.com/v1/chat/completions"
API_KEY = ANTHROPIC_API_KEY
MODEL = "deepseek-chat"


def _call_api(system_prompt, user_message, max_tokens=MAX_TOKENS, max_retries=2):
    for attempt in range(max_retries):
        try:
            resp = requests.post(
                API_URL,
                headers={
                    "Authorization": f"Bearer {API_KEY}",
                    "Content-Type": "application/json",
                },
                json={
                    "model": MODEL,
                    "messages": [
                        {"role": "system", "content": system_prompt},
                        {"role": "user", "content": user_message},
                    ],
                    "max_tokens": max_tokens,
                    "temperature": TEMPERATURE,
                },
                timeout=8,
            )
            data = resp.json()
            if "choices" in data:
                text = data["choices"][0]["message"]["content"].strip()
            elif "error" in data:
                raise RuntimeError(data["error"].get("message", "API error"))
            else:
                raise RuntimeError(f"Unexpected response: {json.dumps(data, ensure_ascii=False)[:200]}")

            # Try to parse as JSON, but if it fails, use the raw text as response
            if text.startswith("```"):
                text = text.strip("`").strip()
                if text.startswith("json"):
                    text = text[4:].strip()
            try:
                return json.loads(text)
            except json.JSONDecodeError:
                # AI returned plain text instead of JSON — use it directly
                return {"response": text, "emotion": "", "root_cause": ""}
        except (KeyError, IndexError) as e:
            if attempt == max_retries - 1:
                raise
            time.sleep(2 ** attempt)
        except requests.RequestException as e:
            if attempt == max_retries - 1:
                raise RuntimeError(f"网络连接失败: {e}")
            time.sleep(2 ** attempt)
    return {"response": "", "emotion": "", "root_cause": ""}


