def get_symptom_advice(symptom):
    """Get a random varied response for a symptom."""
    import random as _random
    advices = SYMPTOM_ADVICE.get(symptom)
    if not advices:
        return {
            "message": "嗯…你说的这个我不太了解，但我很愿意听 👂 可以多说说是什么感觉吗？",
            "options": [],
            "type": "text",
        }
    advice = _random.choice(advices)
    advice_type = "dessert" if "desserts" in advice else ("soundscape" if "soundscapes" in advice else "text")
    return {
        "message": advice["message"] + "\n\n" + advice["suggestion"],
        "options": advice.get("desserts") or advice.get("soundscapes") or [],
        "type": advice_type,
    }
